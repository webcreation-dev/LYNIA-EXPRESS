    <H2>Par où commencer</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "paytosignupxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#paytosignup">Dois-je payer pour m'inscrire? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "paytosignup">
       Non, l'inscription est gratuite et ne nécessite pas de carte de crédit. Nous offrons un compte gratuit et l'abonnement premium peut être réglé mensuellement ou à annuellement..
Si vous choisissez le forfait premium, les frais d'abonnement ne commenceront que lors de votre premier envoi ou de votre première demande de service d'aide à l'achat. Les tarifs de l'abonnement premium sont de 10 USD par mois ou juste de 60 USD par an.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "whencanistartxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#whencanistart">À partir de quel moment puis-je commencer à utiliser vos services? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "whencanistart">
       Immédiatement après avoir terminé votre inscription et vérifié votre email adresse. Vous pouvez utiliser votre adresse Lynia Delivery Express pour vos achats en ligne dans n'importe quel magasin et nous envoyer ainsi votre premier colis.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "form1583xTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#form1583">Qu'est-ce que le formulaire 1583 et pourquoi en ai-je besoin? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "form1583">
       USPS (United States Postal Service) exige ce formulaire pour tout courrier, magazine et journaux qui nécessitent un envoi.<br>
<br>
<strong>Remarque importante :</strong> Le formulaire doit être entièrement rempli et signé par vos soins et accompagné d’une signature notariée (effectuée par un notaire). Veuillez également envoyer l'original avec 2 pièces d'identité : permis de conduire et passeport par exemples.
<br><br>
<strong>Envoyez le fornulaire à l’adresse suivante : </strong>
<br>
Lynia Delivery Express LLC,
<br>
444 Alaska Avenue,
<br>
Torrance, CA 90503 USA
<br>
<br>
<a href="#" target="_blank">Téléchargez le formulaire 1583 pour l’entrepôt situé à Torrance (PDF)</a>
<br><br>
<a href="#" target="_blank">Téléchargez le formulaire 1583 pour l’entrepôt situé à Tualatin (PDF)</a>
<br>
<br>
Besoin d’aide ?  Cliquez <a href="#" target="_blank">ici</a>.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "usaddressxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#usaddress">Quelle adresse aux États-Unis sera fournie ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "usaddress">
       <P>
Vous recevrez un numéro de suite unique, qui peut être utilisé dans n'importe lequel de nos entrepôts.</P>
<P>
Avec un compte gratuit, votre adresse par défaut est notre entrepôt en Californie, qui offre une durée de stockage des colis de 7 jours. Vous pouvez également expédier vers nos entrepôts en Autriche et au Japon.</P>
<P>
Si vous choisissez un abonnement Premium, vous recevrez une adresse par défaut dans notre entrepôt hors taxes de vente en Oregon. Cependant, vous pouvez choisir d'expédier vers l'un de nos entrepôts. En tant que membre Premium, vous bénéficierez également de 45 ou 60 jours de stockage dans n'importe quel entrepôt. </P>*
*Le stockage dépend de votre niveau d'adhésion<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "how-to-enter-tax-idxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#how-to-enter-tax-id">Où dois-je saisir mon numéro d'identification fiscale ou  celui de mon entreprise ?  <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "how-to-enter-tax-id">
       Les douanes de certains pays exigent qu'un identifiant fiscal ou commercial soit imprimé sur la facture commerciale du colis. Si c’est votre cas, un avertissement s'affichera sur votre compte lors de la sélection du mode d'expédition. Selon les exigences de votre pays, il vous sera demandé soit votre propre numéro d'identification fiscale soit celui de votre entreprise .
<br>
<br>

Pour saisir votre identifiant fiscal ou professionnel dans votre profil, veuillez suivre les étapes suivantes :
<br>
<br>
<ol>
<li>Ouvrez votre compte</li>
<li>Cliquez sur "Mon compte"</li>
<li>Sous "Paramètres du compte", cliquez "Modifier mon carnet d’adresses"</li>
<li>Modifiez votre adresse actuelle <strong> OU </strong> cliquez sur  “Ajouter une nouvelle adresse”
<li>Vous verrez une option permettant de saisir le numéro d'identification fiscale ou numéro d'identification de l'entreprise dans le coin inférieur droit.</li></ol><BR>


      </DIV>
     </DIV>

