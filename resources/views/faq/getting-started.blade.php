     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "salestaxxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#salestax">Quelles taxes de vente américaines par État dois-je payer et dois-je régler directement auprès du vendeur ou à Lynia Delivery Express ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "salestax">
       La taxe de vente américaine par État dépend du vendeur auprès duquel vous passez votre commande et de l'entrepôt sélectionné pour vos envois. Notre entrepôt de Tualatin en Oregon, est entièrement exempt de taxe de vente, car l'État de l'Oregon ne fait pas payer de taxe de vente américaine. <br><br>Vous payez des taxes, au moment de l’achat, pour l'État vers lequel vous expédiez. <br><br>Vous payez les taxes de votre pays lorsque l'article arrive dans votre pays.<BR>


