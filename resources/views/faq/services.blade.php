    <H2>Service d’aide à l’achat</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "assistedpurchasexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#assistedpurchase">Comment fonctionne le service d’aide à l’achat et combien coûte-t-il? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "assistedpurchase">
       Nous offrons de faire les achats à votre place quand vous rencontrez des difficultés à passer vos commandes en ligne. Les articles sont achetés en utilisant les fonds de votre compte Lynia Delivery Express. Lors de votre première demande d'aide à l’achat, nous devrons tout d’abord procéder à la vérification de votre identité et de votre mode de paiement avant de pouvoir commencer.<br><br>

La taxe de vente et les frais d'expédition sont déterminés par le vendeur (le cas échéant). Les tarifs correspondant à ce service sont indiqués ci-dessous :<br><br>

<ul><li>Frais de service : 8.50 USD par lot de 10 articles (en provenance du même vendeur)</li>
<li>Frais de traitement (4.3% de la commande globale)</li>
<li>Frais de commission (8% de la commande globale) </li></ul>

Pour en savoir plus, veuillez consulter la page du service d’aide à l’achat ou bien regarder cette <a href="#" target="_blank">vidéo</a>.<BR>


      </DIV>
     </DIV>

