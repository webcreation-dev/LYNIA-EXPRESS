    <H2>Regroupement</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howto-consolrequestxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howto-consolrequest">Comment puis-je faire une demande de regroupement ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howto-consolrequest">
       Notre service de regroupement (rassemblement) de colis coûte 3.25 USD par paquet pour les membres Premium et 5.25 USD par paquet pour les comptes gratuits. Ces frais s’ajoutent aux frais de traitement.<br><br>

Pour placer une demande d’aide de l’achat, veuillez suivre les étapes suivantes :<br><br>

<ol><li>Ouvrez votre compte</li>
<li>Cliquez sur "Mes colis"</li>
<li>Puis sur "Colis dans l’entrepôt"</li>
<li>Puis "Prêt à l’envoi"</li>
<li>Et enfin sur "Commencez un regroupement"</li>
<li>Sélectionnez une adresse de livraison</li>
<li>Sélectionnez les colis que vous souhaitez regrouper</li></ol>

<strong>Remarques importantes</strong>:<br><br>

<ul><li>Vous devez absolument sélectionner deux colis minimum</li>
<li>Remplissez le formulaire de déclaration en douane pour chaque colis que vous souhaitez inclure dans le regroupement (rassemblement). Nous ne pouvons ni regrouper ni expédier de colis sans ces informations.</li>
<li>Le regroupement (rassemblement) n’est possible que si les colis se trouvent dans le même entrepôt.</li></ul><BR>
       <IMG SRC = "shipito/imgs/help/11%20How%20do%20I%20place%20a%20consolidation%20request%250D%250A.pn" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "consolfeesxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#consolfees">Y a-t-il des frais associés au service de regroupement?  <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "consolfees">
       Oui, des frais de regroupement de 3.25 UDS par paquet sont appliqués pour les membres Premium et de 5.50 UDS par paquet pour les comptes gratuits. Vous pouvez changer votre abonnement à tout moment en ouvrant les paramètres de votre compte.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "consolshippingxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#consolshipping">Les frais d'envoi n’apparaîssent pas lorsque je fais ma demande de regroupement. Pourquoi? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "consolshipping">
       Les tarifs d'expédition pour un colis regroupé ne peuvent être déterminés qu’une fois le regroupement terminé. Les tarifs d'expédition et les transporteurs disponibles sont basés sur les dimensions et le poids du colis. Ces informations apparaîtront sur votre compte une fois le regroupement terminé et une nouvelle identification (ID) sera alors attribuée à votre colis. Vous pouvez obtenir une estimation à partir de notre calculateur de frais d'expédition.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howconsolworksxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howconsolworks">Que pensez-vous de ces 6 étapes pour le regroupement (rassemblement) de vos colis ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howconsolworks">
       Le regroupement (rassemblement) de vos colis consiste à prendre des emballages de petites tailles et à les regrouper dans un emballage unique de taille plus grande. Veuillez suivre les 6 étapes indiquées ci-dessous puis cliquer sur "effectuer le paiement". Une fois le paiement effectué, il faut prévoir environ 2 jours ouvrables pour le traitement de votre demande.<br><br>

Merci de bien vouloir suivre les étapes suivantes :<br><br>

<ol><li>Sélectionnez une adresse de livraison</li>
<li>Sélectionnez des colis puis choisissez une option de regroupement pour chacun d’eux

<ul><li>Conserver l’emballage extérieur - Nous plaçons le colis tel que nous l’avons reçu dans un emballage plus grand .</li>
<li>Conserver l'emballage d’origine du produit - Nous jetons l’emballage extérieur et plaçons le produit avec son emballage d’origine dans une seule boîte.</li>
<li>Produit seul - L’emballage d’origine du produit est jeté. Seul le produit est placé dans le carton.</li></ul></li>

<li>Choisissez une méthode d’envoi et une assurance</li>
<li>Sélectionnez les services supplémentaires de votre choix associés à votre demande de regroupement</li>

<ul><li>Supprimez les étiquettes de prix 1 $ USD pour les membres Premium, 2 $ pour les comptes gratuits. Les frais comprennent 5 minutes de travail. Après 5 minutes, des frais de main-d'œuvre de 15 $/30 minutes vous seront facturés.</li>
<li>Procéder au regroupement en urgence : 3.25 USD pour les membres Premium, 5.25 USD pour les comptes gratuits</li>
<li>Papier bulle supplémentaire : 2 USD pour les membres Premium, 4 USD pour les comptes gratuits</li>
<li>Demande spéciale : 5 USD pour les membres Premium, 7 USD pour les comptes gratuits</li>
<li>Emballage personnalisé selon mes besoins : 3 USD pour les membres Premium, 6 USD pour les comptes gratuits </li></ul>

<li>Effectuez le paiement (le service de regroupement ne sera effectué qu’une fois le paiement terminé)</li>
<li>Vérifiez votre demande une dernière fois avant de cliquer sur "Effectuer le paiement"</li></ol>

Pour en savoir davantage sur le processus de regroupement, vous pouvez regarder cette <a href="#" target="_blank">vidéo</a>.<BR>
