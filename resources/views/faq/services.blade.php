       Cela vous donne une idée des articles contenus dans votre colis. Vous pouvez ainsi vérifier que les bons articles ont été reçus et transférés par Lynia Delivery Express.<br>
Ce service peut prendre jusqu'à 2 jours (du lundi au vendredi aux États-Unis, à l'exclusion des jours fériés américains)<br><br>

Il existe deux options possibles concernant les photos :<br><br>

<strong>Demande d’une photo unique</strong><br>
Lynia Delivery Express ouvrira votre colis et prendra une photo de son contenu. Cela vous donnera une idée des articles contenus dans votre colis. Ne vous attendez pas à une photo détaillée. En règle générale, il s'agit d'une photo montrant tous les articles contenus dans l'emballage étalés sur une table.<br><br>

Si le contenu du colis ne rentre pas dans un angle de 91X91cm, nous facturerons automatiquement une demande de photos multiples.<br><br>

<strong>Demande de photos multiples</strong><br>
Cette demande vous donne des photos rapprochées et détaillées des articles contenus dans le colis. Nous prendrons également une photo de la facture ou de la liste d’articles (si présente). Mais aussi, des photos des étiquettes pour les vêtements et autres articles afin de vérifier qu’il s’agit des bonnes tailles. <br><br>

Nous prenons des photos en fonction de notre meilleur jugement. Pour toutes exigences spécifiques, veuillez utiliser l’option Autres instructions et nous expliquer dans les détails comment vous souhaitez que la photo soit prise. Pour les demandes de photos uniques ou de photos multiples, nous ne defaisons pas les produits scellés en usine et nous ne prenons pas de photos de la boîte du produit. Si vous souhaitez que nous inspections le produit et prenions des photos plus détaillées, veuillez utiliser l'option Autres instructions.<br><br>

Pour faire une demande de photos, veuillez suivre les étapes suivantes :<br><br>

<ol><li>Ouvrez votre compte</li>
<li>Cliquez sur "Mes colis"</li>
<li>Puis sur "Colis dans l’entrepôt"</li>
<li>Identifiez le colis en question puis cliquez sur "Ajouter des services supplémentaires"</li>
<li>Choisissez la demande de photo que vous souhaitez, puis cliquez sur « Ajouter ce service »</li></ol>

