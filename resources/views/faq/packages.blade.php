    <H2>Colis</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "dimweightxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#dimweight">Qu’est-ce que le poids dimensionel (DIM)? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "dimweight">
       Le poids DIM fait référence aux dimensions du colis et à la manière dont ces dimensions sont utilisées dans le calcul du poids du colis, c’est à dire en fonction de la taille plutôt que du « poids brut ». Pour fixer le coût des expéditions à l’internationales, les transporteurs facturent le plus élevé du poids réel ou du poids DIM selon la taille de la boîte. Prenons un colis de grande taille (46 x 76 x 10 cm) pesant 2 lb (0,907 gr), tel un oreiller par exemple, certains transporteurs le factureront de la même manière qu’un colis pesant 13 lb (5,9 kg) . Les calculs de poids DIM varient selon le transporteur. Pour vérifier les formules de calcul de chaque transporteur, veuillez consulter notre page des Méthodes d'expédition.<br><br>Pour en savoir plus sur le poids DIM, vous pouvez aussi regarder cette <a href="#" target="_blank">vidéo</a>.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "weightlimitsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#weightlimits">Quelle sont les limites de poids et de taille pour chaque transporteur ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "weightlimits">
       Les limites de poids et de taille varient selon les transporteurs. Pour des informations plus précises, veuillez consulter notre page concernant les différentes <a href="#" target="_blank">Méthodes d’expédition</a>.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "heavypackagefeesxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#heavypackagefees">Y a-t-il des frais supplémentaires associés aux colis lourds ou de longue taille ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "heavypackagefees">
       Oui, des frais supplémentaires sont appliqués pour tout colis dont le poids est supérieur ou égal à 30 lbs (13,6 kg). <br>
<br>
<b><u>Suppléments pour colis lourds</u></b><br>
<ul>
<li>entre 30 lb (13,6 kg) et 69 lb (31,3 kg) – 10 USD</li>
<li>entre 70 lb (31,75 kg) et 99 lb (44,9 kg) – 20 USD</li>
<li>entre 100 lb (45,36 kg) et 149 lb (67,59 kg) – 30 USD</li>
<li>au dessus de 150 lb (68 kg) – 50 USD</li>
</ul>
<br>
<b><u>Frais supplémentaires appliqués par UPS</u></b><br>
<ul>
<li>Au niveau international</li>
<ul>
<li>Des frais supplémentaires de 25.50 USD pour colis volumineux + frais pour colis lourds sont appliqués pour tout colis dont le poids dépasse 70 lb (31,75 kg)</li>
<li>Des frais supplémentaires DIM de 50 USD sont appliqués pour tout colis dont la longueur maximale est supérieure ou égale à 40 pouces (101,6 cm).</li>
</ul>
</ul>
<ul>
<li>Au niveau national</li>
<ul>
<li>Des frais supplémentaires de 25.50 USD pour colis volumineux + frais pour colis lourds sont appliqués pour tout colis dont le poids dépasse 50 lb (22,68 kg)</li>
<li>Des frais supplémentaires DIM de 50 USD sont appliqués pour tout colis dont la longueur maximale est supérieure ou égale à 40 pouces (101,6 cm).</li>
</ul>
</ul>
Si votre colis pèse par exemple 89 lb (40,37), le transporteur vous facturera des frais pour colis volumineux de 25.50 USD et un supplément de 20 USD pour colis lourds.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "storagexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#storage">Combien de temps mon colis peut-il rester au sein de l’entrepôt? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "storage">
       <P>
Les comptes gratuits peuvent stocker leurs colis pendant 7 jours maximum dans nos entrepôts en Californie, en Oregon, en Autriche et au Japon. Les membres Premium bénéficient de 45 ou 60 jours de stockage selon leur niveau d'adhésion. Les membres Premium peuvent stocker leurs colis jusqu'à 90 jours dans tous les entrepôts (Torrance, Californie, Tualatin, Oregon, Autriche et Japon) si vous choisissez le stockage prolongé comme avantage. Si vous n'envoyez pas votre colis par la poste avant l'expiration de la période de stockage, vous avez la possibilité d'acheter jusqu'à 28 jours de stockage supplémentaire. Si vous choisissez de prolonger la durée de stockage d'un colis expiré, vous devrez payer des frais de 7 USD plus 1 USD par jour pendant lequel le colis a expiré. Cela prolongera votre stockage de 7 jours supplémentaires.</P>
<strong>Remarque importante :</strong> la durée de stockage ne peut être prolongée que 4 fois, soit jusqu'à un total de 28 jours. Passé cette date, le colis sera considéré comme abandonné et sera immédiatement éliminé.
<P>

Nous nous efforçons de vous informer de tout colis approchant de sa date d'expiration. Pour être sûr (e) que ces notifications arrivent dans votre boîte de réception, assurez-vous que support@cs.shipito.com a bien été ajouté à votre carnet d'adresses ou à votre liste blanche. 28 jours après la date d'expiration d'origine, le colis sera considéré comme abandonné et sera immédiatement éliminé. </P><br><br>Pour en savoir plus, vous pouvez regarder cette <a href="#" target="_blank">vidéo</a>.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "pickupxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#pickup">Suis-je autorisé(e) à venir chercher moi-même mon colis à votre entrepôt ou à y déléguer quelqu’un? Comment puis-je m’y prendre? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "pickup">
       <P>
Oui, vous ou toute personne désignée (ami / membre de la famille) pouvez venir chercher votre colis sur place dans tous nos entrepôts, pour un montant de 20 $ par colis. Votre colis devra être identifié avec la méthode de livraison suivante : «Local Pickup» (retrait sur place). Les 20 USD de retrait sur place ainsi que tous les autres frais de service en cours devront être payés avant le retrait. Vous pourrez retirer votre colis munis d’une pièce d'identité valide avec photo. Toute personne déléguée pour retirer votre colis à votre place doit être répertorié dans votre compte.
</P>

<P>
Heures d'ouverture pour le retrait sur place :
<UL>
 <LI>Torrance: du lundi au vendredi, de 10h à 17h PDT</LI>
<LI>Tualatin: du lundi au vendredi, de 10h à 17h PDT</LI>
<LI>Chiba : le retrait local n'est pas disponible à l'entrepôt du Japon</LI>
</UL></P><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "whereismypackagexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#whereismypackage">Le statut de mon colis indique "Livraison à l’entrepôt faite", mais cela n’apparaît pas dans mon compte. Où est mon colis?  <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "whereismypackage">
       <P>
Plusieurs milliers de colis arrivent chaque jour à notre entrepôt. Selon la période de l'année, il peut s'écouler jusqu'à 2 jours ouvrables après l’arrivée d’un colis à l’entrepôt avant qu’il ne figure dans votre compte (du lundi au vendredi aux États-Unis, à l'exception des jours fériés américains). Si votre colis n'apparaît pas dans votre compte après 2 jours ouvrables, il est possible que nous n'ayons pas pu traiter votre colis.
</P>
<P>
Si vous pensez que c'est le cas, veuillez soumettre un ticket d'assistance en fournissant le numéro de suivi et toutes autres informations supplémentaires susceptibles de faciliter notre enquête.</P><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "nosuitexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#nosuite">Que se passe-t-il si j’ai oublié mon numéro de suite au moment de passer commande en ligne avec mon adresse aux États-Unis?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "nosuite">
       Veuillez immédiatement saisir votre colis et son numéro de suivi dans la section ¨Colis attendu¨ de votre compte pour aider à le localiser. Lorsqu'aucun numéro de suite n’est indiqué sur un colis ou lorsque le nom indiqué sur le colis ne correspond pas au numéro de suite, le colis sera placé dans la section des colis non réclamés. Des photos de son contenu seront prises pour aider à identifier son propriétaire et le formulaire de déclaration en douane sera complété par nos soins. Des frais de taitement pour les photos et pour la déclaration en douane seront attribués au colis. Si votre colis a bien été livré mais qu’il n'apparaît pas dans votre compte d’ici 2 jours ouvrables, veuillez soumettre un ticket d'assistance, en fournissant le numéro de suivi et toutes autres informations supplémentaires susceptibles de faciliter notre enquête.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "undeliverablexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#undeliverable">Mon transporteur déclare que la livraison de mon colis n’a pas pu avoir lieu. Que s’est-il passé? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "undeliverable">
       <P>
Le transporteur a sans doute essayé de livrer le colis sur nos heures de fermeture ou le dimanche. Veuillez demander à votre transporteur de faire la livraison entre 9h et 17h, du lundi au vendredi, exception faite des jours fériés américains.
</P><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "notifyonarrivalxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#notifyonarrival">Allez-vous m‘avertir de l’arrivée de mon colis à votre entrepôt? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "notifyonarrival">
       Nous vous enverrons un e-mail dès que votre colis aura été enregistré et saisi dans votre compte. Assurez-vous de vérifier vos spam si vous ne recevez pas d'e-mail.
<br> <br>
Une notification apparaîtra aussi dans votre compte même si vous ne recevez pas nos e-mails.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "afterarrivalxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#afterarrival">Que dois-je faire une fois que mon colis arrive à l’entrepôt? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "afterarrival">
       Une fois que votre colis arrive dans notre entrepôt, vous devez cliquer sur action requise, sélectionner votre colis et remplir le formulaire de déclaration en douane. C‘est aussi à ce moment là que vous pouvez soumettre vos demandes spéciales. Une fois la déclaration en douane et les demandes spéciales effectuées, le colis peut être programmé pour l'envoi. Sélectionnez alors prêt à expédier. Choisissez votre transporteur, l'assurance, puis réglez l'envoi de votre colis.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "restrictionsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#restrictions">Existe-t-il des restrictions quant aux articles que je peux envoyer via Lynia Delivery Express? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "restrictions">
       Oui, veuillez consulter la liste des <A href="#" target="_blank">articles interdits ou soumis à des restrictions</A>.
<br>
<br>
<strong>Remarque importante :</strong>Cette liste n'est pas exhaustive et ces restrictions changent constamment. Nous faisons de notre mieux pour mettre cette page à jour au fur et à mesure que nous recevons des notifications de nos transporteurs, mais au final, c’est à vous qu’il revient de vous assurer que les articles que vous achetez sont conformes aux restrictions gouvernementales en vigueur et à celles des transporteurs.
<br>
<br>
Il peut parfois être tout à fait légal d’acheter certains articles aux États-Unis, mais tout à fait illégal de les expédier dans votre pays. N’hésitez pas à consultez le service client pour toutes questions sur les articles interdits ou soumis à des restrictions.
<br><br>
Pour en savoir davantage, vous pouvez regarder cette <a href="#" target="_blank">vidéo</a>.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "mpsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#mps">Le vendeur m'a envoyé plusieurs colis alors même que je n'ai passé qu'une commande. Allez-vous me facturer chaque colis individuellement? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "mps">
       Oui. Les frais de traitement sont de 2.25 USD pour les membres Premium et de 3.25 USD pour les comptes gratuits pour chaque colis enregistrés sur votre compte. Ces frais sont ajoutés au coût total des colis expédiés séparemment. Pour réduire ces frais de traitement de colis, de nombreux vendeurs vous permettent de choisir que vos achats soient expédiés dans le moins de boîtes possible.<BR>


