    <H2>Fraudes</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "retailerxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#retailer">Je suis vendeur et je suspecte que l’un de vos clients soit impliqué dans un marché frauduleux. Pouvez-vous m’aider? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "retailer">
       Lynia Delivery Express prend le marché des fraudes très au sérieux et fait tout ce qui est en son pouvoir pour lutter contre. Nous avons mis des systèmes et des procédés en place afin de protéger nos clients mais aussi les vendeur qui expédient vers nos entrepôts.
<br>
<br>
Nous avons un règlement extrêmement strict et sévère contre le marché des fraudes. Tous les comptes suspectés de se livrer à des actions frauduleuses sont automatiquement placés sous surveillance. Selon ce que nous y découvrons, le compte peut être annulé.
<br>
<br>
Si, en tant que vendeur, vous suspectez une transaction frauduleuse, veuillez s’il vous plaît <a href="#">nous en informer</a> immédiatement. Nous souhaitons nous mettre en relation avec vous pour régler ce problème et éviter qu’il se reproduise.<BR>


