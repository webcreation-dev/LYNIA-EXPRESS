    <H2>Réclamations</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "lost_undelivered_damagedxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#lost_undelivered_damaged">Comment puis-je déclarer un colis perdu, non livré, ou endommagé? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "lost_undelivered_damaged">
       1. Ouvrez votre compte
<br>
<br>
2. Trouvez le paquet en question et sélectionnez les options à sa droite (comme dans l‘exemple ci-dessous).
<br>
<br><BR>
       <IMG SRC = "shipito/fr/imgs/help/6382f90b1be2a08030cbede56a82e2be.png" BORDER = "0" CLASS = "img-responsive">

        <br>
3. Un formulaire va apparaître (comme ci-dessous) sur lequel vous pourrez sélectionner, parmi trois problèmes différents, celui qui vous concerne. Veuillez suivre les instructions indiquées sur le formulaire une fois que vous avez sélectionné votre problème.
<br><BR>
        <IMG SRC = "shipito/fr/imgs/help/5a2585d7efd4384dc6efc9188317d1f8.png" BORDER = "0" CLASS = "img-responsive">

        <br>
<br>
4. une fois le formulaire envoyé, un ticket d’assistance va apparaître sur votre compte. Nous traiterons votre réclamation et vous tiendrons au courant de son évolution par le biais de ce ticket d’assistance.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "incorrectpackagexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#incorrectpackage">J'ai reçu un mauvais colis. Que faire? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "incorrectpackage">
       <P>
Si un colis qui ne vous appartient pas apparaît dans votre compte, veuillez créer un ticket d'assistance. Le personnel de l'entrepôt supprimera alors le colis de votre compte.
</P>
<P>
S‘il y a erreur de colis, merci de créer un ticket d'assistance et de nous envoyer des photos des étiquettes et du contenu du colis. Nous vous fournirons ensuite des instructions supplémentaires.
</P><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "warehouse_damagedxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#warehouse_damaged">Que faire si mon colis arrive à l‘entrepôt endommagé ou s‘il y manque des articles? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "warehouse_damaged">
       Lynia Delivery Express prend toujours grand soin d‘informer ses clients de tous dommages suspects à l‘arrivée. Mais c‘est réellement votre responsabilité de vous assurer que tous les articles nous arrivent en bon état.
<br>
<br>
Veuillez vous souvenir que tous les colis arrivent des différents transporteurs sur palettes. Nous ne pouvons donc pas inspecter chaque colis au moment de la livraison. Nous vous demandons de vérifier les photos fournis à l‘arrivée. Et si vous souhaitez des photos supplémentaires pour voir l‘intérieur du colis, il faut en faire la demande.
<br>
<br>
Nous ne pouvons pas accepter de réclamation pour un colis arrivé endommagé à l‘entrepôt, même si le vendeur refuse votre réclamation.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "lost_within_the_warehousexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#lost_within_the_warehouse">Que faire si mon paquet est perdu dans l‘entrepôt ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "lost_within_the_warehouse">
       Si par hasard nous ne sommes pas en mesure de retrouver votre colis, il sera marqué comme ¨perdu dans l‘entrepôt¨. Nous le rechercherons alors activement sur une période de 3 à 4 jours ouvrables. Dans le cas où il resterait introuvable, vous en serez informé par le biais d‘un ticket d‘assistance et le processus de réclamation pourra commencer.
<br>
<br>
Il ne vous sera rien demandé tant que le processus de réclamation ne sera pas entamé. Vous devrez ensuite nous fournir la facture du vendeur avec le montant exact des articles perdus. Ce n‘est que lorsque nous verrons la facture du vendeur que nous pourrons vous rembourser. Il faut compter 3 à 4 jours ouvrés pour le remboursement.
<br>
<br>
Si votre colis est retrouvé et que le remboursement n‘a pas encore eu lieu, il sera réintroduit sur votre compte et vous recevrez une notice d‘information. Si votre colis est retrouvé mais que le remboursement a dejà eu lieu, vous pourrez choisir soit de conserver le remboursement soit de le retourner afin que votre colis soit réintroduit sur votre compte.
<br>
<br>
Veuillez noter que le montant du remboursement se fera sur la base de la valeur déclarée. <strong>Il est de votre responsabilité de déclarer de manière complète et précise l‘arrivée de tous vos colis.</strong> J‘espère que vous comprenez également que nous ne pouvons pas offrir de compensation pour le retard causé par la perte de votre colis. Remarque : le remboursement se fait sur la base du prix le plus bas, celui que vous avez déclaré ou celui indiqué sur la facture.

De même, si un colis arrive sans numéro de suite ou si le nom indiqué et le numéro de suite ne correspondent pas, le colis est placé dans notre panier MIA jusqu‘à la réception de documents valides prouvant la propriété du paquet. Nous procèderons également à l‘ouverture du paquet pour la prise de photos du contenu et la vérification de la corrélation avec la facture.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "photos_takenxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#photos_taken">Pourquoi des photos de l‘intérieur du colis ont-elles été prises alors que je ne l‘avais pas demandé? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "photos_taken">
       Toutes les fois que vous achetez une assurance pour dommages potentiels, des photos de l‘intérieur du colis sont prises comme preuves de l‘intégrité du colis et de son contenu avant de quitter l‘entrepôt. Si vous avez demandé une assurance pour dommages potentiels durant la livraison et qu‘aucune photo n‘a encore été prise, une photo du contenu sera placée pour un montant minimum de 5 USD pour les comptes premium et de 8 USD pour les comptes gratuits. Si un incident arrive par la suite à votre paquet et que vos articles sont endommagés, nous pourrons prouver l‘état de votre paquet avant son envoi et procéder ainsi à une réclamation. <BR>


      </DIV>
     </DIV>


    <H2>Assurance</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "insurance_for_my_packagesxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#insurance_for_my_packages">Que dois-je faire pour être sûr(e) que mon colis est bien assuré? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "insurance_for_my_packages">
       Lors de la soumission de votre demande d'envoi, les options suivantes apparaîtront : assurance livraison, assurance pour dommages et livraison ou aucune assurance.
<br>
<br>
Veuillez noter que, même si des frais d'assurance sont inclus dans les frais d'expédition, vous devez toujours sélectionner le type d'assurance souhaité pour que votre colis soit couvert <strong>(reportez-vous à “Quels sont les taux de protection de ces deux assurances?”ci-dessous pour découvrir nos types d‘assussurance).</strong>
<br>
<br>
Lorsque l'assurance est sélectionnée, votre colis est couvert par Lynia Delivery Express. Vous n'aurez pas à déposer de réclamation auprès du transporteur vous-même, nous travaillerons avec les transporteurs en votre nom pour traiter toute réclamation.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "insurance_not_availablexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#insurance_not_available">Pourquoi n‘y a t-il pas d‘assurance disponible? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "insurance_not_available">
       Nous n‘offrons pas d‘assurance pour les colis expédiés par USPS First Class International et Airmail Economy.
<br>
<br>
Si vous voulez assurer votre colis mais que vous ne voyez pas l‘option assurance s‘afficher pour le mode de livraison que vous avez sélectionné, veuillez sélectionner un autre mode de livraison.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "two_types_of_insurancexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#two_types_of_insurance">Que couvrent les deux types d’assurance ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "two_types_of_insurance">
       <strong>Assurance livraison :</strong> Cette assurance couvre la valeur du contenu et les frais d'expédition de tout colis perdu pendant le transport. Cela ne couvre que les colis perdus. Cela ne couvre pas les colis saisis par la douane, refusés et retournés, abandonnés, etc. et si votre colis est livré, les éventuels articles manquants ou endommagés ne seront pas couverts.
<br>
<br>
<strong>Assurance pour dommages potentiels :</strong> Cette assurance couvre le contenu de votre colis en cas de perte ou de dommages d’articles pendant le transit. Il ne couvre que la valeur des articles endommagés ou manquants. Si un article endommagé est réparable, nous vous remboursons les frais de réparation à condition qu'ils ne dépassent pas la valeur de l'article. Les frais d'expédition ne sont pas remboursés pour les colis livrés, mais des exceptions sont faites si la plupart ou la totalité des articles du colis sont endommagés ou manquants.
<br>
<br>
Lynia Delivery Express vous offre la possibilité d’acheter ou bien l’assurance livraison seule ou bien l’association des deux : assurance dommages potentiels et assurance livraison.
<br>
<br>
Les frais d'assurance sont calculés en fonction de la valeur que vous déclarez sur la déclaration de douane, et le montant que vous déclarez est le montant qui vous sera remboursé. Cependant, veuillez noter que nous ne pouvons pas vous rembourser plus que ce qui a réellement été
payé au vendeur pour l'article en question. Pour chaque réclamation, nous vous demanderons une preuve du prix d'achat, facture ou reçu du vendeur.
<br>
<br>
Tous les frais perçus par Lynia Delivery Express (frais de traitement, frais de regroupement, etc...) ne sont pas remboursables et ne peuvent être couverts par aucune assurance.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "insurancexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#insurance">Quels sont les articles qui ne sont pas couverts par l'assurance ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "insurance">
       Nos transporteurs ne proposeront pas d'assurance pour les produits suivants :<br><br>
<ul><li>Articles restreints ou interdits</li>
<li>Antiquités, œuvres d'art et argenterie</li>
<li>Tout moyen de paiement autre qu'en espèces (par exemple, chèques, cartes de crédit prêtes à l'emploi, timbres fiscaux, titres, instruments négociables, billets du Trésor, etc.)</li>
<li>Toutes les pierres précieuses et métaux précieux</li>
<li>Dessins, modèles, plans, actes, manuscrits et tous autres documents</li>
<li>Fourrures, vêtements en fourrure</li>
<li>Articles ménagers et effets personnels</li>
<li>Bijoux</li>
<li>Argent de toute nature (par exemple, espèces, billets de banque, pièces de monnaie, billets de banque)</li>
<li>Produits périssables et sensibles à la température</li>
<li>Timbres, billets, etc.</li>
<li>Emballage de vente au détail de collection</li></ul><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "lost_undeliveredxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#lost_undelivered">Quand puis-je déposer une réclamation pour colis perdu ou non livré? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "lost_undelivered">
       Vous ne pouvez déposer de réclamation que si vous avez sélectionné et payé la couverture d'assurance sur votre colis au moment de l'envoi. Veuillez noter que les délais de demandes d'indemnité et de traitement varient selon les compagnies de transport.
<br>
<br>
 <strong>Vous trouverez votre compagnie de transport ci-dessous:</strong>
<br>
<li> <strong>USPS et Priority Parcel:</strong> Vous pouvez déposer une réclamation dans les 30 jours suivant la commande mais pas plus tard que 60 jours ouvrables.
</li>
<br>
<li> <strong>Airmail Priority:</strong> Il faut attendre au mois 60 jours ouvrables avant de déposer votre réclamation, mais pas plus de 90 jours ouvrables. **
</li>
<br>
** Pour les colis à destination du Brésil, il faut attendre au moins 90 jours ouvrables, mais pas plus de 120 jours ouvrables avant de déposer votre réclamation.
<br>
<li> <strong>Tous les autres transporteurs:</strong> Veuillez nous informer dès que vous remarquez un problème, nous accordant ainsi plus de chance pour régler tout problèmes ou retard possible. Le délai pour déposer votre réclamation est de 30 jours après l'expédition du colis.
</li><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "damaged_missingxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#damaged_missing">Quand puis-je déposer une réclamation pour colis endommagé ou perdu? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "damaged_missing">
       Le délai pour soumettre une réclamation pour tout article endomagé ou manquant au sein d‘un colis délivré est de 10 jours à compter de la date de livraison.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "insurance_coveragexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#insurance_coverage">Quels documents dois-je fournir avec ma réclamation? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "insurance_coverage">
       La facture fournit par le vendeur à l’achat, ou n’importe quelle preuve de l’achat, est obligatoire pour déposer toutes réclamations.  Le montant et la date de l’achat doivent être indiqués, ainsi que l’adresse de livraison (adresse de l’entrepôt) et le numéro de suivi du colis.
<br>
<br>
<strong>Produits manquants et endommagés :</strong>
<br>
<br>
Veuillez conserver l’emballage jusqu’à ce que votre réclamation aboutisse. Nous aurons besoin de photos de l’intérieur et de l’extérieur du colis, ainsi que de tout articles endommagés.
<br>
<br>
<strong>Envois par le biais de USPS et RRD :</strong>  Il nous faut le rapport de votre bureau de poste local ou du service des douanes confirmant que la paquet a bien été livré avec des articles manquants ou abîmés. Veuillez inspecter le paquet lors de la livraison pour alerter le livreur au besoin. Nous ne pouvons pas accepter de plaintes sans ce document avec USPS.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howlongxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howlong">Combien de temps devrai-je attendre pour que ma réclamation soit traitée? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howlong">
       Le traitement des réclamations varient selon les services de transportation.
<br><br>
<strong>Airmail Priority, USPS et Lynia Delivery Express Priority Parcel</strong> sont traités dans les 30 jours ouvrables à partir de la date de réception de tous les documents requis.
<br><br>
<strong>Tous les autres service de transportation</strong>sont traités dans les 5 à 15 jours ouvrables à partir de la date de réception de tous les documents requis.<BR>


      </DIV>
