    <H2>FAQ sur le partenariat</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "theAffiliateProgramxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#theAffiliateProgram">Comment puis-je m'inscrire au programme de partenariat? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "theAffiliateProgram">
       Pour vous inscrire au programme d'affiliation :
<br>
<br>
<ol><li>Connectez-vous à votre compte Lynia Delivery Express. Si vous n'en avez pas déjà un, vous devrez d'abord le créer.</li>
<li>Cliquez sur "Mon compte"</li>
<li>Cliquez sur "Mon compte affilié"</li>
<li>Cliquez sur "Inscription en tant qu'affilié"</li>
<li>Cliquez sur "CRÉER MON COMPTE AFFILIÉ"</li>
<li>Vous pourrez désormais voir votre lien/URL de parrainage d'affilié sous "Mon tableau de bord d'affiliation"</li>
<li>Copiez ce lien/URL et commencez à le partager partout où vous pensez que les gens l'utiliseront</li></ol><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howtoearnmoney-affiliatexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howtoearnmoney-affiliate">Comment puis-je gagner de l‘argent avec le programme de partenariat? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howtoearnmoney-affiliate">
       Earning money with the Lynia Delivery Express Affiliate Program is easy.  When you sign-up for the program you will be given a URL which includes your affiliate ID. Share this URL as a hot link on your website, social media, blog or wherever you plan to promote Lynia Delivery Express. When your followers click on the link, and sign up for a Lynia Delivery Express account they will be credited to your account. Once they ship a package you will earn 10% of the shipping fees. As that customer ships more packages you will receive additional money at certain intervals. To learn more click <a href="#">HERE</a>.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howtoregister-affiliatexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howtoregister-affiliate">Devrais-je utiliser mon identifiant de partenaire affilié ou puis-je enregistrer un domaine? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howtoregister-affiliate">
       Lorsque vous vous inscrivez à notre programme de partenariat, vous pouvez utiliser les liens avec un identifiant de partenaire affilié ou enregistrer votre nom de domaine dans votre compte de partenaire affilié. Lorsque vous enregistrez votre nom de domaine, nous devons être à même de vérifier votre propriété par le biais du WHOIS. Vous ne pouvez pas cacher votre identité avec les paramètres de confidentialité. Une fois que vous avez enregistré votre nom de domaine, notre base de données surveillera toutes les références de votre site Web qui resteront dans notre base de données pendant 60 jours. Si le client effectue son inscription dans les 60 jours, il sera pris en compte dans vos commissions de partenaire affilié.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "registershipitodomain-affiliatexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#registershipitodomain-affiliate">Puis-je enregistrer un nom de domaine Lynia Delivery Express dans mon pays? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "registershipitodomain-affiliate">
       Non, sauf si vous obtenez une autorisation écrite préalable de Lynia Delivery Express.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "shipitowebsite-affiliatexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#shipitowebsite-affiliate">Faut-il que je crée un site internet Lynia Delivery Express dans ma langue? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "shipitowebsite-affiliate">
       NON. Ne créez pas de site internet Lynia Delivery Express. Nous n'autorisons pas l'utilisation du nom <strong> SHIPITO </strong> dans une URL ou dans un nom de domaine sans notre autorisation écrite préalable. Nous n'autorisons pas non plus l'utilisation du logo Lynia Delivery Express sans notre autorisation écrite.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howcanigetpaid-affiliatexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howcanigetpaid-affiliate">Comment mes commissions du programme de partenariat sont-elles payées? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howcanigetpaid-affiliate">
       Les demandes de paiement d'affiliation ne peuvent pas inclure les commissions d'affiliation du mois en cours et doivent inclure toutes les commissions une fois le mois terminé. Nous n'acceptons pas les demandes de paiement pour les commissions partielles gagnées au cours du mois en cours.
<br><br>
Par exemple, les demandes de paiement pour toutes les commissions gagnées en mai doivent être effectuées en juin. Vous pouvez soumettre des demandes pendant plusieurs mois, à condition que les commissions demandées aient été gagnées au cours des mois précédant la demande. Dans ce scénario, les paiements des commissions gagnées en avril, mai et juin peuvent être demandés dès le 1er juillet.
<br><br>
Les demandes de paiement approuvées seront payées dans les 7 à 10 jours ouvrables à compter du 1er du mois.<BR>


      </DIV>
