    <H2>Interface du compte</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "passwordrequirementsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#passwordrequirements">Quelles sont les exigences relatives au mot de passe? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "passwordrequirements">
       Votre mot de passe doit contenir:<br><br>
<ul><li>au moins 8 caractères</li>
<li>y compris une lettre minuscule,</li>
<li>une lettre majuscule</li>
<li>et un chiffre ou un caractère spécial</li></ul><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "wheretofindaddressxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#wheretofindaddress">Où puis-je trouver mon adresse Lynia Delivery Express? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "wheretofindaddress">
       Une fois sur votre compte, vous trouverez votre adresse dans la section intitulée "Mon adresse dans l’entrepôt". <br><br>

<ol><li>Ouvrez votre compte</li>
<li>Allez sur "MON ADRESSE DANS L’ENTREPÔT"</li>
<li>Cliquez sur "COPIER MON ADRESSE"</li>
<li>Puis sur "COPIER TOUTE L’ADRESSE".</li></ol>

Votre numéro de suite reste le même, quel que soit l'entrepôt de Lynia Delivery Express vers lequel votre colis est expédié. N'oubliez pas de TOUJOURS inclure votre numéro de suite Lynia Delivery Express. Si le site internet propose une adresse 2 ou un espace pour un numéro d'appartement, ajoutez-y votre numéro de suite. Un colis sans numéro de suite entraîne retard, frais supplémentaires, frais de photo de contenu et peut empêcher le colis d’être pacé sur votre compte.<BR>
       <IMG SRC = "shipito/fr/imgs/Where-do-I-find-my-shipito-address-French.png" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "manageaddressesxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#manageaddresses">Où puis-je renseigner l’adresse de facturation et de livraison ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "manageaddresses">
       Pour ajouter une adresse de facturation et/ou de livraison à votre compte :
<br><br>
<ol><li>Ouvrez votre compte</li>
<li>Allez sur "Mon compte" </li>
<li>Cliquez sur "Paramètres du compte" </li>
<li>Puis sur "Modifier mon carnet d’adresses"</li>
<li>Et enfin sur  "Ajouter une nouvelle adresse".</li></ol><BR>
       <IMG SRC = "shipito/fr/imgs/Where-do-I-manage-my-billing-and-shipping-addresse" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "membershipxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#membership">Comment puis-je changer mon type d’abonnement? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "membership">
       Pour modifier le type de votre abonnement : <br><br>

<ol><li>Ouvrez votre comte</li>
<li>Aller sur "Mon compte"</li>
<li>Cliquez sur "Paramètres du compte"</li>
<li>Puis sur "Mon abonnement" </li>
<li>Puis "MODIFIER MON ABONNEMENT"</li>
<li>Sélectionnez l’abonnement désiré.</li></ol><BR>
       <IMG SRC = "shipito/fr/imgs/How-can-I-change-my-account-membership-plan-French" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "othernamesxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#othernames">Puis-je recevoir des colis sous un autre nom? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "othernames">
       Oui, vous pouvez recevoir des colis sous plus d'un nom dans votre suite, le nom doit cependant être répertorié dans la section : noms supplémentaires de votre compte. Vous pouvez avoir jusqu'à 6 noms supplémentaires sur votre compte. Veuillez noter que si vous êtes invité(e) à vérifier votre compte, vous devrez fournir des pièces d’identité officielles pour chaque nom figurant sur votre compte.<br><br>

Pour ajouter un nom supplémentaire à votre compte : <br><br>
<ol><li>Ouvrez votre compte </li>
<li>Allez sur "Mon compte" </li>
<li>Cliquez sur "Paramètres du compte"</li>
<li>Puis sur "Noms supplémentaires"</li>
<li>Saisissez le nom que vous souhaitez ajouter à votre compte</li>
<li>Cliquez sur le signe "+" pour l’ajouter à votre compte. </li></ol><BR>
       <IMG SRC = "shipito/fr/imgs/Can-I-receive-packages-under-another-name-French2" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "support_ticketxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#support_ticket">Comment puis-je ouvrir un ticket d‘assistance? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "support_ticket">
       Pour ouvrir un ticket d’assistance :
<br><br>
<ol><li>Ouvrez votre compte</li>
<li>Allez sur "Mon compte"</li>
<li>Cliquez sur "Aide pour mon compte"</li>
<li>Puis sur "Tickets d’assistance"</li>
<li>Puis "Ouvrir un nouveau ticket d’assistance" </li>
<li>Sélectionnez l’option qui décrit le mieux votre situation</li>
<li>Tapez vos messages dans la zone de texte qui apparaît.</li></ol>

Une fois votre ticket soumis, quelqu’un du service client examinera votre message et vous répondra dans les 4 jours ouvrables selon le calendrier américain.<BR>
       <IMG SRC = "shipito/fr/imgs/How-do-I-open-a-support-ticket-French.png" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "current_and_past_support_ticketsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#current_and_past_support_tickets">Où puis-je consulter mon ticket d’assistance ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "current_and_past_support_tickets">
       Pour consulter votre ticket d’assistance :<br><br>
<ol><li>Ouvrez votre compte</li>
<li>Allez sur "Mon compte</li>
<li>Cliquez sur "Aide pour mon compte"</li>
<li>Puis sur "Tickets d’assistance"</li>
<li>Sélectionnez "Voir les détails" positionné en face de votre ticket d’assistance.</li></ol><BR>
       <IMG SRC = "shipito/fr/imgs/How-do-I-view-my-support-tickets-French.png" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "accountholdxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#accounthold">Pourquoi mon compte se trouve-t-il en attente? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "accounthold">
       Plusieurs raisons peuvent expliquées le fait que votre compte se trouve en attente. Vous devriez trouver un ticket d’assistance exceptionnel dans votre compte expliquant le problème et ce qu’il faudrait faire pour le résoudre.<br>
Si vous ne trouvez pas de ticket d’assistance exceptionnel, veuillez alors en créer un.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "cancelaccountxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#cancelaccount">Comment puis-je clôturer mon compte? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "cancelaccount">
       Dans le cas où vous souhaitez clôturer votre compte, sachez que nous serons tristes de vous voir partir. Nous espérons que vous nous aiderez à comprendre  les raisons pour lesquelles vous voulez fermer votre compte. Pour lancer une clôture de compte, il vous faudra ouvrir un ticket d'assistance (comme illustré ci-dessous) dans lequel vous pourrez indiquer les raisons de votre décision, ainsi que toutes informations supplémentaires dont vous disposez s’y rapportant. Veuillez vous assurer que vous ne laissez aucun solde sur votre compte et qu’aucun colis ne se trouve dans, ou est en route vers, notre entrepôt. Avant la clôture de votre compte, assurez-vous également que tous vos colis vous sont bien parvenus. Une fois votre compte clôturé, et si vous souhaitez que vos données soient supprimées, vous devez envoyer un e-mail à Privacy@shipito.com et y faire la demande expresse. Cette demande devra inclure votre numéro de suite.<BR>
       <IMG SRC = "shipito/fr/imgs/How-can-I-cancel-my-account-French2.png" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Lynia Delivery ExpressaddressxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Lynia Delivery Expressaddress">Comment puis-je modifier mon adresse Lynia Delivery Express? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Lynia Delivery Expressaddress">
       Pour changer votre adresse Lynia Delivery Express par défaut : <br><br>
<ol><li>Ouvrez votre compte </li>
<li>Allez sur "Mon compte"</li>
<li>Cliquez sur "Paramètres du compte"</li>
<li>Puis sur "Modifier mon carnet d’adresses"</li>
<li>Choisissez l’adresse que vous souhaitez utiliser.</li></ol>

Veuillez noter que seuls les comptes avec un abonnement premium peuvent utiliser l'entrepôt de Tualatin en Oregon.<BR>
       <IMG SRC = "shipito/fr/imgs/How-do-I-change-my-Lynia Delivery Express-address-FrenchREV.jpg" BORDER = "0" CLASS = "img-responsive">

      </DIV>
