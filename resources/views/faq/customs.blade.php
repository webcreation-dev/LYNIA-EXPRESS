    <H2>Chine : droits de douane, taxes et limites d'importation</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "cndutiesassementxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#cndutiesassement">Qui fixent les droits de douane et les taxes ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "cndutiesassement">
       Les douanes chinoises fixent les droits de douane et les taxes par rapport à la valeur totale de votre colis.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "cndutiespaidxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#cndutiespaid">À quel moment se fait le réglement des droits de douane et des taxes ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "cndutiespaid">
       Le réglement des droits de douane et des taxes au gouvernement chinois se fait au moment de la livraison.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "cnlimitsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#cnlimits">Quelles sont les limites d’importation ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "cnlimits">
       <P>
Pour des envois personnels, l'importation de colis contenant plus d'un article est limitée à 1 000 CNY. Les colis d’une valeur supérieure à 1 000 CNY, et contenant plus d’un article, sont dédouanés telle une importation commerciale. Les colis d'une valeur supérieure à 1 000 CYN, contenant plus d'un article, et déclarés être pour l’usage personnel sont retournés aux États-Unis. Si le colis ne contient qu'un seul article, il n'y a pas de limite de valeur, cependant les douanes chinoises se réservent le droit d'approuver l'importation d'un colis ou de le refuser et de le renvoyer aux États-Unis, qu'il y ait 1 ou plusieurs articles dans le colis.
</P>
<P>
Si vous importez des articles d'une valeur de 20 000 CYN ou plus par an, tous les envois doivent être dédouanés en tant qu'importation commerciale. </P>
<BR>


      </DIV>
