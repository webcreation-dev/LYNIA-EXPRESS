     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "subjectodutiesxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#subjectoduties">Mon colis est-il soumis aux droits de douane et/ou taxes? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "subjectoduties">
       Oui, vous êtes financièrement responsable des droits de douane et /ou des taxes encourus. Pour plus d'informations, nous vous suggérons de contacter votre bureau de douane local, car les droits de douane et de taxes dépendent de la destination, de la provenance et de la valeur des articles expédiés. Ces réglementations peuvent être modifiées à tout moment.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "usdutiesxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#usduties">Dois-je payer des taxes et des frais de douane si mon colis arrive chez Lynia Delivery Express depuis un autre pays? Comment puis-je effectuer ce paiement ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "usduties">
       <P>
Oui, vous êtes responsable du paiement de tous les dédouanements, droits et taxes de douane. Ceux-ci peuvent être payés de deux manières :
</P>
<UL>
  <LI> Règlement direct : en contactant le transporteur directement. </LI>
  <LI> Règlement à la livraison : en contactant Lynia Delivery Express 2 à 3 jours avant l'arrivée de votre colis, indiquant le nom du transporteur, le montant dû et le numéro de suivi. Puis, en déposant les fonds suffisants sur votre compte pour que nous puissions effectuer le paiement au moment de la livraison. </LI>
</UL>
<P>
Si les droits et taxes de douane n’ont pas été préalablement réglés et si vous ne disposez pas des fonds suffisants sur votre compte pour couvrir ces frais, les colis peuvent être refusés et renvoyés à l'expéditeur.
</P><BR>


      </DIV>
     </DIV>

