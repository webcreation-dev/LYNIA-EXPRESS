      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "giftormerchandisexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#giftormerchandise">Que dois-je déclarer pour le contenu de mon colis : marchandise personnelle, marchandise commerciale ou cadeau ? <SPAN CLASS = "ga-caret"></SPAN></A>
