    <H2>Douanes</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "eeiformxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#eeiform">Qu’est-ce qu’un formulaire EEI? Est-il nécessaire? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "eeiform">
       <p>Concernant les exportations américaines dont la valeur d'un seul produit dépasse 2 500 USD, l’Electronic Export Information(EEI) est généralement exigé des douanes et de la protection des frontières des États-Unis. </p>
<p> Les réglementations pour colis coûteux sont complexes. Nous vous recommandons fortement de diviser votre colis en plusieurs pour que la valeur totale de chaque colis reste inférieure à 2 500 USD. Si le reconditionnement n'est pas possible, il nous faudra préparer un formulaire EEI (Electronic Export Information) pour vous. Des frais de 40 USD sont appliqués pour ce service. De plus, les colis nécessitant un formulaire EEI doivent être expédiés via des transporteurs spécifiques qui seront disponibles pour l'envoi. </p><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howtofillcustomsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howtofillcustoms">Comment compléter le formulaire de déclaration en douane ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howtofillcustoms">
       Le formulaire de déclaration en douane doit être rempli pour chaque colis individuellement, peu importe l’endroit de leur destination. Ce formulaire n'est pas seulement utilisé pour le dédouanement international, mais aussi par l'assurance en cas de problèmes. Elle se basera sur le montant indiqué sur le formulaire de douane.<br><br>

Nous ne pouvons pas vous expédier vos colis tant que le formulaire de déclaration en douane n’est pas rempli. Pour en savoir plus, vous pouvez consulter cette <a href="#" target="_blank">vidéo</a>.<br><br>

Pour remplir votre formulaire de déclaration en douane, veuillez suivre les étapes ci-dessous :<br><br>

<ol><li>Ouvrez votre compte</li>
<li>Cliquez sur "Colis dans l’entrepôt"</li>
<li>Puis sur "Remplir une déclaration en douane"</li>
<li>Remplissez le formulaire de déclaration en douane</li></ol>
<br>
<strong>Les pays suivants n'autorisent pas l'importation d'articles usagés :</strong><br><br>
<ul><li><SMALL> Brésil</SMALL></li>
<li><SMALL> Afrique du sud</SMALL></li>
<li><SMALL> Uganda</SMALL></li>
<li><SMALL> Vietnam</SMALL></li>
<li><SMALL> Indonésie</SMALL></li></ul>
<br>
<strong>REMARQUE IMPORTANTE:</strong><br>
<ul><li><SMALL>Vous n'éviterez pas les éventuels droits et taxes en sélectionnant CADEAU.</SMALL></li>
<li><SMALL>Vous devez indiquer si le colis contient des piles ou des marchandises dangereuses.</SMALL></li>
<li><small>Si vous n’avez pas déclaré le montant exact, vous devez corriger le montant de la déclaration avant d'expédier votre colis.</small></li>
<li><small>Tout remboursement se fera sur la valeur déclarée, il y va donc de votre responsabilité de vous assurer que la déclaration en douane de chaque colis, dès sa réception, est remplie de manière complète et détaillée. Comprenez également que nous ne pouvons offrir aucune compensation pour les retards dû à ¨colis perdus¨. Le remboursement est basé sur le montant minimum indiqué de la valeur déclarée ou facturée.</small></li></ul>
Un colis dont la déclaration en douane n’est pas remplie ressemble à la capture d'écran ci-dessous :<BR>
