    <H2>TPS (taxe sur les produits et services) australienne</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "au-gst-whathaschangedxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#au-gst-whathaschanged">Ce qui a changé le 1er juillet 2018 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "au-gst-whathaschanged">
       À compter du 1er juillet 2018, une taxe australienne sur les produits et services (TPS) sera facturée aux consommateurs et aux entreprises pour tous les articles importés en Australie.
<br>
<br>
Les consommateurs australiens sont désormais tenus de payer une taxe TPS de 10% sur les articles de «valeur modérée» (inférieure ou égale à 1 000 dollars australiens) achetés auprès de vendeurs internationaux. Lynia Delivery Express perçoit la taxe TPS sur ces achats et la verse directement au gouvernement australien en votre nom. La taxe TPS est calculée en fonction de la valeur des articles expédiés, à cela s’ajoute les frais de service et d'envoi de Lynia Delivery Express.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "augstwhatdoesitmeanxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#augstwhatdoesitmean">Comment cela s’applique-t-il aux services de Lynia Delivery Express ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "augstwhatdoesitmean">
       <strong> Pour tout achat d’une valeur égale ou supérieure à $1,000 AUD </strong>
<br>
<br>
<ul><li>Lynia Delivery Express <strong> NE </strong> perçoit <strong> PAS </strong> la TPS et vous êtes tenu d’effectuer le paiement directement au bureau des impôts australiens. </li></ul>
<br>
<strong> Pour tout achat d’une valeur inférieure ou égale à $1,000 AUD </strong>
<br>
<br>
<ul><li>Si un ABN (numéro d'entreprise australien) est indiqué dans votre dossier Lynia Delivery Express, vous n'aurez pas à payer de taxes lors de l’envoi, mais pourrez soumettre ces frais au gouvernement australien lors du règlement trimestriel habituel de vos taxes.</li>
<li>Si vous AUCUN ABN (numéro d'entreprise australien) n’est indiqué dans votre dossier Lynia Delivery Express, une TPS de 10% est appliquée pour les frais de port et de traitement au moment de l'envoi.</li></ul><BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "augstamountxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#augstamount">À combien s’élève la TPS ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "augstamount">
       10% de frais <font size="2">(sur la valeur des articles importés, payés au gouvernement australien)</font>
<br>
<strong>+</strong>
<br>
Frais de 1/11 (sur les services) + 1/11 (sur l’assurance) + 1/11 (sur les coûts d’envoi)
<br>
<br>
Remarque importante : la valeur du contenu et les frais d'expédition étant deux choses différentes, 2 TPS distinctes s’affichent au moment de l’envoi.
<BR>


      </DIV>
     </DIV>

