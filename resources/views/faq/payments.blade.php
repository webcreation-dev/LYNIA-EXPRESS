     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "paymenttimingxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#paymenttiming">Combien de temps faut-il avant qu‘un versement prenne effet? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "paymenttiming">
       Le temps de traitement d‘un versement varie selon la méthode de paiement. La plupart des versements par cartes de crédit ou PayPal sont traités immédiatement, même si certaines transactions peuvent prendre de 15 minutes à quelques heures parfois. Les versements par Bitcoin, une fois effectués dans votre portefeuille, prennent généralement environ une heure avant d‘apparaître sur votre compte Lynia Delivery Express, et même parfois jusqu‘à 48 heures. Les virements bancaires prennent 5 à 7 jours ouvrables, une fois vérification de l‘envoi faite par votre banque, avant d‘être ajoutés à votre compte.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "predepositxTOP">
