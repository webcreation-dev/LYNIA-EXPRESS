    <H2>Paiement</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howtomakepaymentxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howtomakepayment">Comment puis-je effectuer un paiement? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howtomakepayment">
       Identifiez-vous puis cliquez sur"+ FAIRE UN VERSEMENT" sous "Solde des comptes."  Vous devrez ensuite sélectionner votre mode de paiement.
<br>
<br>
<strong> Virement bancaire</strong> —  Tout virement bancaire s‘accompagne de frais d‘un montant de 16 USD.  Une fois le paiement initié, les détails et les informations concernant le paiement vont s‘afficher à l’écran. Il vous faudra apporter ces informations à votre banque ou utiliser leur portail en ligne pour initier le virement bancaire. Lynia Delivery Express ne prendra pas contact avec votre banque et n‘effectuera pas le transfert à votre place. Faites attention de bien utiliser le “Numéro de Réference/Commande” comme indiqué sur le borderau de transfert, conformément aux instructions spécifiques ou numéro de référence de votre banque. Sans cela, votre paiement ne pourra pas être appliqué à votre compte.  Veuillez effectuer un nouveau paiement auprès de Lynia Delivery Express pour chaque transfert que vous voulez envoyer afin de recevoir un “Numéro de réference/commande” actualisé. Sans cela, le paiement ne pourra pas être appliqué à votre compte. Sachez que nous n‘acceptons pas les paiements en provenance de tiers. S‘il y a la moindre disparité dans la saisie de votre nom, adresse, code postal ou toute autre information de paiement, nous requérerons des documents supplémentaires pour vérification (photo d‘identité, confirmations de vos informations bancaires etc...) et le virement bancaire ne sera appliqué à votre compte que lorsque la vérification sera terminée.
<br>
<br>
<strong>Carte de crédit</strong> — Il vous suffit de saisir votre numéro de carte bancaire ainsi que vos données de facturation.
Dans certains cas, vous remarquerez que l‘argent a été débité de votre carte mais que cela n‘apparaît pas sur votre compte Lynia Delivery Express. Cela se prodiut chaque fois que l‘entreprise traitant votre carte de crédit  effectue un blocage temporaire pour des raisons de sécurité le temps de vérifier le paiement. Le processus prend généralement deux à trois jours ouvrables, après quoi, soit les fonds nous sont transférés soit ils retournent sur votre carte, selon les résultats de leurs recherches. Tout ce processus est effectué par votre banque, Lynia Delivery Express n‘a aucun contrôle sur la fréquence et la durée de vérification des paiments.
<br>
<br>
Vous verrez parfois un message d‘erreur apparaître alors que vous essayer d‘effectuer un paiement. Dans la majeure partie des cas, c‘est parce que l‘entreprise traitant votre carte de crédit  a trouvé une disparité dans le paiement. Nous conseillons alors soit d‘utiliser une autre carte de crédit soit de contacter votre banque pour régler le problème. Nous ne pouvons pas traiter manuellement une carte qui a été refusée par le système. Après trois tentatives de paiement, veuillez interrompre vos essais et contacter le service client de Lynia Delivery Express pour vous faire aider.  Sachez que si vous utilisez une carte de crédit délivré à Taiwan, il vous faudra prendre contact auprès du service client par le biais  d‘un ticket d‘assistance et vérifier votre carte manuellement avant de pouvoir l‘utiliser pour un paiement.
<br>
<br>
<strong> PayPal</strong> —  Vous serez redirigé vers le site Internet de PayPal où il vous sera demandé de confirmer le paiment après vous être identifié. Veuillez vous assurer que le nom indiqué sur la carte ou compte PayPal est bien le même que celui indiqué sur votre compte Lynia Delivery Express. S‘il comporte l‘initiale du second prénom, ou un préfixe ou un suffixe, il faut les saisir dans la fenêtre de paiement et cela doit correspondre aux informations contenues sur votre compte Lynia Delivery Express. De plus, les informations de votre compte PayPal doivent apparaître en anglais. Les caractères étrangers ne sont pas traduits pas notre système, et le rejet du paiement en résultera.
<br>
<br>
<strong> Bitcoin</strong>  — Une fois le paiement Bitcoin effectué vers votre compte Lynia Delivery Express, un lien apparaît sur votre écran.  Après avoir cliqué dessus, vous serez redirigé vers votre portefeuille pù vous pourrez compléter le paiement. N‘essayez pas de copier le lien et de le placer dans votre portefeuille.  Ça ne fonctionnera pas correctement. Le lien ne fonctionnera que si votre portefeuille est ouvert et actif sur votre appareil.  Sinon, vous recevrez un message d‘erreur ou le lien ne fonctionnera tout simplement pas.  Vous devez faire un paiement minimum de 1 USD pour que le système accepte de terminer la transaction, sinon vous recevrez un message d‘erreur.  Cela prend généralement environ une heure avant de voir l‘argent apparaître sur votre compte Lynia Delivery Express, une fois le paiement terminé dans votre portefeuille. Il arrive parfois cependant que cela prenne jusqu‘à  48 heures.
<br>
<br>
<strong> Boleto</strong> — Une fois le paiement Boleto effectué vers votre compte Lynia Delivery Express, vous serez redirigé sur le site Internet de Boa Compra où vous recevrez votre bordereau Boleto et des instructions à suivre.<BR>


