<ul><li>Les cartes de crédit (Visa, American Express, Master Card, JCB, et China Union Pay)</li>
<li>PayPal</li>
<li>Bitcoin</li>
<li>Les virements bancaires</li>
<li>Boleto Bancario (for Brazil)</li>
<li>Poli (pour l’Australie)</li>
<li>Sofort (pour l’Europe)</li></ul>
Nous <strong>n’</strong>acceptons  <strong>pas</strong> les paiements en espèces, même pour les retraits sur place.<BR>


      </DIV>
