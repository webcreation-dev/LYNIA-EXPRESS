      <DIV CLASS = "panel-title" ID = "I_try_to_use_my_credit_cardxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#I_try_to_use_my_credit_card">Pourquoi reçois-je un message d’erreur de paiement et pourquoi ma carte de crédit ne fonctionne-t-elle pas ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "I_try_to_use_my_credit_card">
       Vous trouverez ci-dessous la liste des raisons les plus courantes pour lesquelles le système affiche un message d'erreur lorsqu'il tente d'effectuer un paiement :<br><br>
<ul><li>La société qui traite votre carte de crédit a identifié une anomalie dans le paiement. Nous vous recommandons d'utiliser une autre carte ou bien de contacter votre banque concernant ce problème.</li>
<li>Le fournisseur de la carte bloque les paiements internationaux. Veuillez demander à votre banque ou à la société qui vous fournit la carte de crédit de bien vouloir autoriser les paiements internationaux. Veuillez également vérifier que le nom et l'adresse de facturation de votre compte sont corrects. </li>
<li>Vous avez peut-être cliqué trop rapidement sur le bouton de retour ou avez actualisé la page alors que le paiement était toujours en cours de traitement.</li>
<li>Si le symbole de chargement continue de s’inscrire passé les 5 minutes, nous vous recommandons de fermer la page, de vous reconnecter à votre compte, puis de réessayer.</li>
<li>Vous devez peut-être contacter votre banque et vérifier la sécurité 3D configurée pour votre carte de crédit.</li>
<li>Après 3 échecs de paiement, vous devez attendre 2 heures avant d'effectuer un nouveau paiement. Les 2 heures commencent à partir du dernier échec de paiement</li></ul>
Pour votre sécurité, veuillez <strong>NE PAS</strong> inclure le numéro complet de votre carte de crédit. <br><br>
<strong>Remarque importante</strong> :
N'oubliez pas que lorsque vous utilisez une carte de crédit de Taïwan, vous devez contacter le service client via un ticket d'assistance et vérifier manuellement votre carte de crédit avant de pouvoir l'utiliser pour effectuer un paiement.<br><br>
Si le problème persiste, veuillez contacter notre équipe d'assistance afin d’obtenir de l'aide.<BR>


