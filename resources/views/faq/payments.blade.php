     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "paymentnotshowingxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#paymentnotshowing">Pourquoi mon paiement n‘apparaît-il pas sur mon compte Lynia Delivery Express? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "paymentnotshowing">
       <P>
Selon votre méthode de paiement, nous vous demandons d‘attendre 24 h après que l‘intervale de temps ci-dessus ne se soit écoulé avant de contacter le service client de Lynia Delivery Express. Si le paiement n‘apparaît toujours pas sur votre compte, veuillez soumettre un ticket d‘assistance avec les informations suivantes :
</P>
<UL>
 <LI><strong>Virement bancaire</strong> – Veuillez nous fournir le nom apparaissant sur votre compte bancaire, la date et l‘heure de la transaction, l‘appellation de la banque qui a initié le transfert, le montant du transfert en dollars USD, le numéro de référence / commande utilisé dans les instructions spécifiques, et une copie de la feuille de confirmation du transfert. C‘est un document différent que le reçu qui vous a été donné par la banque au moment du transfert. Nous aurons besoin d‘un numéro de confirmation authentique pour retrouver le transfert perdu. Vous pouvez l‘obtenir auprès de votre banque.</LI>
 <LI><strong>Carte de crédit</strong> – Veuillez nous fournir le montant du dépôt en dollars USD, les six premiers et les quatre derniers numéros de la carte utilisée, le jour et l‘heure de la transaction, le nom et l‘adresse de facturation de la carte de crédit, et une capture d‘écran des fonds prélevés sur le compte de votre carte de crédit. Pour des questions de sécurité, veuillez s‘il vous plait <strong>NE PAS</strong> envoyer le numéro complet de votre carte de crédit.</LI>
 <LI><strong>PayPal</strong> – Veuillez nous fournir l‘email PayPal utilisé, le montant du dépôt en dollars USD, le code unique de l‘identification de la transaction PayPal, la date et l‘heure de la transaction, et une capture d‘écran des fonds prélevés de votre compte PayPal. </LI>
<LI><strong>Bitcoin</strong> – Veuillez nous fournir la date et l‘heure du paiement, le montant du dépôt en dollars USD, et le numéro unique de la commande Lynia Delivery Express. </LI>
<LI><strong>Autres méthodes de paiement</strong> – Veuillez nous fournir le code d‘identification unique de la transaction de la méthode de paiement, le montant du dépôt en dollars USD,  la date et l‘heure de la transaction, et une capture d‘écran des fonds prélevés sur votre compte.</LI>
</UL><BR>


      </DIV>
