    <H2>Remboursement</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "requestarefundxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#requestarefund">Comment puis-je effectuer une demande de remboursement? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "requestarefund">
       <font size="2">
<strong>REMARQUE IMPORTANTE :</strong>
<br>
<li>Tous vos colis DOIVENT être expédiés. Il n'est pas possible de procéder à un remboursement tant que des colis sont dans votre boîte de réception. </li>
</font>
<br>
<ol><li>Ouvrez votre compte</li>
<li>Allez sur "Mon compte"</li>
<li>Cliquez sur "Paramètres du compte"</li>
<li>Puis sur "Dépôt sur le compte"</li>
<li>Puis "Demande de remboursement"</li>
<li>Saisissez le montant du remboursement, puis envoyez la demande</li></ol>
<br>
Veuillez noter que toutes demandes de remboursement encourent des frais d'administration de 5 USD. Les remboursements par virement bancaire sont également facturés des frais de virement de retour de 32 USD. Des frais de traitement de 4,3% sont appliqués pour toutes les autres méthodes de remboursement.<BR>
       <IMG SRC = "shipito/imgs/How-do-I-request-a-refund-French.png" BORDER = "0" CLASS = "img-responsive">

      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "receivemyrefundxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#receivemyrefund">Comment mon remboursement va-t-il s‘effectuer? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "receivemyrefund">
       Les remboursements sont effectués par le même procédé que celui utilisé pour les versements, en allant des versements les plus anciens aux plus récents. Cela signifie que vos remboursements peuvent se faire par autant de petits paiements que de méthodes différentes plutôt qu‘un gros remboursemsent à la fois.
<br>
<strong> Veuillez noter:</strong>
<br>
<br>
<strong> Bitcoin</strong> – Une fois que Lynia Delivery Express aura terminé de traiter votre demande de remboursement, vous recevrez un e-mail de Bitpay dans lequel vous trouverez le lien qui vous permettra de réclamer vos fonds. Ce lien est valide pendant 72 heures, il vaut donc mieux s‘y prendre de bonne heure. Si vous dépassez la fenêtre des 72 heures, veuillez contacter le service client de Lynia Delivery Express par le biais d‘un ticket d‘assistance et l‘équipe vous renverra le lien. Remarque: les fonds n‘apparaîtont dans votre portefeuille que lorsque vous aurez utilisé ce lien.  Merci de ne pas contacter le service client de Lynia Delivery Express avant de localiser l‘e-mail et d‘essayer de collecter vos fonds par le biais du lien.
<br>
<br>
<strong>Boleto</strong> – Une fois que Lynia Delivery Express aura terminé de traiter votre demande de remboursement, Boa Compra prendra contact avec vous et vous donnera ses instructions sur la manière de collecter vos fonds.
<br><br>Pour en savoir plus sur le processus de remboursement, vous pouvez regarder ces <a href="#" target="_blank">vidéos</a> qui se trouvent dans l’Académie de Lynia Delivery Express.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "toprocessarefundxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#toprocessarefund">Combien de temps prend un remboursement? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "toprocessarefund">
       Il faut compter 2 à 3 jours ouvrables le temps que notre équipe traite votre demande de remboursement. Une fois terminé, vous recevrez un e-mail de confirmation.
<br>
<br>
<strong> Virement bancaire</strong> — Les remboursements peuvent prendre jusqu‘à trois semaines après que Lynia Delivery Express ait fini de les traiter avant de les voir apparaître sur votre compte en banque.
<br>
<br>
<strong>Carte de crédit</strong> — Les remboursements peuvent prendre deux à trois semaines après que Lynia Delivery Express ait fini de les traiter avant de les voir apparaître sur votre compte en banque.
<br>
<br>
<strong> PayPal </strong> —  Les remboursements peuvent prendre jusqu‘à trois semaines après que Lynia Delivery Express ait fini de les traiter avant de les voir apparaître sur votre compte PayPal.
<br>
<br>
<strong> Bitcoin</strong>  — Une fois que Lynia Delivery Express aura terminé de traiter votre demande de remboursement, vous recevrez un e-mail de Bitpay dans lequel vous trouverez le lien qui vous permettra de réclamer vos fonds. Ce lien est valide pendant 72 heures, il vaut donc mieux s‘y prendre de bonne heure. Si vous dépassez la fenêtre des 72 heures, veuillez contacter le service client de Lynia Delivery Express par le biais d‘un ticket d‘assistance et l‘équipe vous renverra le lien. Remarque: les fonds n‘apparaîtont dans votre portefeuille que lorsque vous aurez utilisé ce lien.  Merci de ne pas contacter le service client de Lynia Delivery Express avant de localiser l‘e-mail et d‘essayer de collecter vos fonds par le biais du lien.
<br>
<br>
<strong>Boleto</strong> – Une fois que Lynia Delivery Express aura terminé de traiter votre demande de remboursement, Boa Compra prendra contact avec vous et vous donnera ses instructions sur la manière de collecter vos fonds.
