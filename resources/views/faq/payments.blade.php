    <H2>Sécurité 3D </H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "What_is_3_D_SecurityxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#What_is_3_D_Security">Qu‘est-ce que Sécurité 3-D (3DS)? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "What_is_3_D_Security">
       3DS est un service de sécurité conçu par des systèmes (VISA, MasterCard, etc) et sélectionné pour être utilisé par différentes banques. Chaque système a son propre nom. Pour Visa c‘est Verified par Visa/Visa Secure, pour MasterCard c‘est SecureCode, pour Discover c‘est ProtectBuy, pour JCB International c‘est J/Secure et pour American Express c‘est American Express SafeKey.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "is_not_set_up_with_3DSxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#is_not_set_up_with_3DS">Que dois-je faire si ma carte de crédit n‘est pas configuré avec 3DS? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "is_not_set_up_with_3DS">
       Si votre banque a choisi d‘utiliser 3DS, vous pouvez faire jusqu‘à cinq (5) tentatives de paiement avec Lynia Delivery Express avant qu‘il ne vous soit demandé de configurer votre carte avec 3DS.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "bank_doesnt_recognize_3DSxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#bank_doesnt_recognize_3DS">Que dois-je faire si ma banque ne reconnaît pas 3DS? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "bank_doesnt_recognize_3DS">
       Parce que 3DS a été conçu par des systèmes mais choisi d‘être utilisé par certaines banques, de nombreux employés de banque ne vont pas le reconnaître comme faisant partie de leur service. Pour vous faire aider, il vous faudra appeler votre banque et demander à parler à l‘équipe responsable de la sécurité. Une fois le contact établi, demander à parler avec quelqu‘un qui connaît bien 3DS. Ça sera généralement un membre chevronné de l‘équipe qui pourra vous aider. Ne vous laissez pas intimider s‘ils vous répondent que ça n‘est pas de leur ressort. Demandez simplement à parler avec un représentant habitué à 3DS jusqu‘à ce que vous trouviez la personne qui pourra vous aider. Sachez que 3DS n‘est pas sous le contrôle de Lynia Delivery Express mais sous celui de la banque qui vous a délivré votre carte.
<BR>


      </DIV>
     </DIV>


    <H2>Boleto - Méthode de paiement brésilienne</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Lynia Delivery ExpresswithBoletoxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Lynia Delivery ExpresswithBoleto">Lynia Delivery Express accepte-t-il Boleto comme méthode de paiement? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Lynia Delivery ExpresswithBoleto">
       Oui. Lynia Delivery Express accepte les paiements par borderaux Boleto. Cette méthode de paiement est valable pour tous les dépôts que vous avez à faire sur votre compte Lynia Delivery Express.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "BoletotopayforAssistedPurchasexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#BoletotopayforAssistedPurchase">Puis-je utiliser Boleto pour régler l‘aide à l‘achat? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "BoletotopayforAssistedPurchase">
       Oui. Une fois votre demande d‘aide à l’achat terminée à partir de votre compte Lynia Delivery Express, nous vous informerons du solde restant. Vous pourrez ensuite effectuer le paiement avec Boleto.
<br>
<br>
<strong>NOTE:</strong> Le traitement du paiement prend 2 à 7 jours ouvrables pour Boleto. Si vous voulez profiter de soldes ou d‘un coupon avec une date d‘expiration, nous vous conseillons d‘utiliser une autre méthode de paiement. Nous ne pouvons pas vous guarantir que le versement prendra effet avant la fin des soldes ou de la date d‘expiration de votre coupon.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "shopatUSAstoresxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#shopatUSAstores">Puis-je utiliser Boleto aux États-Unis? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "shopatUSAstores">
       Non, malheureusement! Boleto est spécifique au Brésil et les magasins américains ne le reconnaissent pas comme une méthode de paiement valide. Mais c‘est accepté par Lynia Delivery Express, et vous pouvez toujours utiliser le service d‘aide à l‘achat si vous souhaitez faire vos achats avec Boleto.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "availableinmyLynia Delivery ExpressAccountxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#availableinmyLynia Delivery ExpressAccount">Combien de temps cela prend-il pour que mes fonds soient disponibles sur mon compte Lynia Delivery Express? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "availableinmyLynia Delivery ExpressAccount">
