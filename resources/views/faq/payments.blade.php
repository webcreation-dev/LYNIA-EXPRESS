     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "for-an-Assisted-PurchasexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#for-an-Assisted-Purchase">Quelles méthodes de paiement puis-je utiliser pour régler le service d‘aide à l‘achat? <SPAN CLASS = "ga-caret"></SPAN></A>
