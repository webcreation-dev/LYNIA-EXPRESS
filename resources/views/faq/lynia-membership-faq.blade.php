    <H2>À propos des récompenses pour les membres Lynia Delivery Express
</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "What_is_Lynia Delivery ExpressxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#What_is_Lynia Delivery Express">Qu’est-ce que l’option de récompense des membres Lynia Delivery Express ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "What_is_Lynia Delivery Express">
       Il s’agit d’un programme qui vous permet de gagner des points en expédiant des colis et en recommandant des amis à Lynia Delivery Express. Gagner des points vous permet d’atteindre les statuts Silver, Gold, Platinum, ou Diamond. Plus le statut des récompenses est élevé, plus vous pouvez choisir d’avantages, tels que les frais d’envoi réduits, une durée d’entreposage supérieure et des services gratuits ou à prix réduits.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_xTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_">Comment puis-je participer au programme de récompense des membres Lynia Delivery Express ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_">
       Les clients Lynia Delivery Express qui achètent un abonnement Premium annuel sont automatiquement inscrits au programme de récompense des membres Lynia Delivery Express.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_is_my_Rewards_statusxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_is_my_Rewards_status">Comment est calculé mon niveau de récompense ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_is_my_Rewards_status">
       Votre niveau de récompenses pour la nouvelle année civile sera déterminé par le nombre total de points accumulés au 31 décembre de l’année civile précédente. Tout au long de la nouvelle année, vous pouvez accéder au niveau de récompenses supérieur en gagnant suffisamment de points. Par exemple : si vous commencez l’année au niveau Silver, vous devrez gagner 20 points pour passer au niveau Gold. Vos points ne sont pas cumulés d’une année sur l’autre.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_participate_in_the_Lynia Delivery Express_MemberxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_participate_in_the_Lynia Delivery Express_Member">Puis-je participer au programme de récompense des membres Lynia Delivery Express avec un abonnement gratuit ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_participate_in_the_Lynia Delivery Express_Member">
       Non, le programme de récompense des membres Lynia Delivery Express n’est disponible que pour les clients ayant un abonnement Premium annuel.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_participate_with_axTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_participate_with_a">Puis-je participer avec un abonnement mensuel Premium ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_participate_with_a">
       Non, le programme de récompense des membres Lynia Delivery Express n’est disponible que pour les clients ayant un abonnement Premium annuel.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_purchase_a_PremiumxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_purchase_a_Premium">Comment acheter un abonnement Premium annuel ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_purchase_a_Premium">
       Connectez-vous à votre compte Lynia Delivery Express. Survolez « Mon compte », cliquez sur « Mon abonnement », puis sélectionnez « Modifier l’abonnement ».
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_convert_my_monthly_membershipxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_convert_my_monthly_membership">Comment convertir mon abonnement mensuel en abonnement Premium annuel ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_convert_my_monthly_membership">
       Connectez-vous à votre compte Lynia Delivery Express. Survolez « Mon compte », cliquez sur « Mon abonnement », puis sélectionnez « Modifier l’abonnement ». Sélectionnez ensuite la mise à niveau au compte Premium (facturé annuellement) au coin inférieur gauche.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_my_Rewards_status_changxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_my_Rewards_status_chang">Mon niveau de récompense peut-il changer tout au long de l’année ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_my_Rewards_status_chang">
       Oui, lorsque vous gagnez des points, vous pouvez passer au niveau de récompense suivant à tout moment de l’année civile. Vous ne pouvez pas descendre à un niveau de récompense inférieur dans le courant d’une année. Par exemple : si vous commencez l’année au niveau Silver, vous devrez gagner 20 points pour passer au niveau Gold.
<BR>


      </DIV>
     </DIV>


    <H2>Gain de points de récompense
</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_buy_RewardsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_buy_Rewards">Puis-je acheter des points de récompenses ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_buy_Rewards">
       Non, ce n’est pas possible pour le moment.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "I_should_have_earned_pointsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#I_should_have_earned_points">J’aurais dû accumuler des points mais ils n’apparaissent pas dans mon compte. À l’aide !
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "I_should_have_earned_points">
       Veuillez prendre contact avec support@shipito.com pour toute question relative aux points de récompenses.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_earn_Lynia Delivery ExpressxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_earn_Lynia Delivery Express">Comment puis-je gagner des points pour le programme de récompense des membres Lynia Delivery Express ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_earn_Lynia Delivery Express">
       Les points peuvent être accumulés comme suit :
<br>
<br>
<li> 1 point : envoi d’un colis pesant plus de 5 lbs</li>
<li> 0,5 points : envoi d’un colis pesant moins de 5 lbs </li>
<li> 0,5 points : 1 recours à l’aide à l’achat </li>
<li> 1 point : achat d’un abonnement annuel</li>
<li> 1 point : recommandation d’un ami qui envoie au moins 1 colis</li>
<li> 1 point : parrainez un ami qui achète un abonnement Premium annuel</li>
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Do_I_earn_Rewards_pointsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Do_I_earn_Rewards_points">Est-ce que je gagne des points de récompense lors de l’achat d’un abonnement mensuel Premium ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Do_I_earn_Rewards_points">
       Non, vous gagnez seulement un point de récompense lors de l’achat d’un abonnement Premium annuel.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "member-returnsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#member-returns">Puis-je obtenir des points sur mes retours? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "member-returns">
       Les retours effectués via Manuel Mailout ne permettront pas de gagner des points. Cependant, si vous retournez un colis en utilisant US Premium Parcel, vous gagnez des points de récompense.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_see_how_manyxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_see_how_many">Comment voir combien de points de récompense j’ai accumulé ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_see_how_many">
       Connectez-vous à votre compte Lynia Delivery Express. Survolez « Mon compte », cliquez sur « Mes récompenses », puis sélectionnez « Détail des points ».
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_earn_Rewards_pointsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_earn_Rewards_points">Puis-je gagner des points de récompense en recommandant des amis à Lynia Delivery Express ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_earn_Rewards_points">
       Oui, vous gagnez 1 point de récompense pour chaque ami parrainé qui expédie au moins un colis. Vous obtenez 1 point supplémentaire s'ils achètent un abonnement annuel Premium et expédient ensuite un colis. Il n'y a pas de limite au nombre d'amis que vous pouvez parrainer.<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_refer_axTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_refer_a">Comment puis-je recommander un ami ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_refer_a">
       Vous pouvez le faire de deux façons. La première consiste à lui donner votre numéro de boîte. Lorsque votre ami s’abonne, une case lui permet de saisir un code de recommandation.  Il leur faudra saisir votre votre numéro de suite dans cette case.
<br>
<br>
La deuxième façon est de donner votre URL. De cette façon, votre numéro de boîte figure déjà dans la case du code de recommandation lors de l’inscription. Pour trouver votre URL, vous devez ouvrir une session sur votre compte Lynia Delivery Express. Survolez ensuite « Mon compte », cliquez sur « Mes récompenses », « Points de récompense », puis sur « Afficher toutes les recommandations ». Cliquez ensuite sur le signe plus (+) à côté de « Recommander un ami ». Vous pourrez y copier votre URL et la transmettre à vos amis.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Where_I_can_see_allxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Where_I_can_see_all">Où puis-je voir toutes mes recommandations ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Where_I_can_see_all">
       Connectez-vous à votre compte Lynia Delivery Express. Survolez « Mon compte », cliquez sur « Mes récompenses », puis sélectionnez « Points de récompense ». Vous verrez alors le nombre de points que vous avez gagnés grâce aux recommandations. Cliquez sur « Afficher toutes les recommandations » pour les voir toutes.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_refer_a_friend_ifxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_refer_a_friend_if">Comment recommander un ami si je ne veux pas donner mon numéro de boîte ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_refer_a_friend_if">
       Pour le moment, vous devez donner votre numéro de boîte. Si vous ne voulez vraiment pas le faire cela, vous pouvez vous inscrire en tant qu’affilié et recevoir une commission sur vos recommandations. Toutefois, elles ne vous rapporteront pas de points.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_accumulate_Rewards_pointsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_accumulate_Rewards_points">Puis-je accumuler des points de récompenses sans abonnement Premium annuel ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_accumulate_Rewards_points">
       Oui, Lynia Delivery Express continue à suivre les points de chaque activité admissible, mais ils ne vous donnent accès à des avantages qu’avec un abonnement Premium annuel.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Do_my_Rewards_pointsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Do_my_Rewards_points">Mes points de récompenses sont-ils transférés d’une année sur l’autre ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Do_my_Rewards_points">
       Non, ils ne sont pas transférés vers l’année civile suivante. Bien que vos points ne soient pas cumulés, ils servent à déterminer votre niveau d’abonnement de l’année suivante.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_transfer_RewardsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_transfer_Rewards">Puis-je transférer des points de récompenses vers un autre compte Lynia Delivery Express ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_transfer_Rewards">
       Non, les points de récompenses ne sont pas transférables d’un compte à un autre.
<BR>


      </DIV>
