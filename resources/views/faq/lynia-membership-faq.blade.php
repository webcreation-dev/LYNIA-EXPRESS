    <H2>Avantages des récompenses des membres
</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_many_benefits_are_earnedxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_many_benefits_are_earned">Les niveaux de récompenses donnent droit à combien d’avantages ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_many_benefits_are_earned">
       <li> Silver: 1 avantage</li>
<li>Gold : 2 avantages </li>
<li>Platinum : 3 avantages </li>
<li> Diamond : 3 avantages plus 5 % de rabais sur tous les envois</li>
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Do_Standard_Lynia Delivery Express_Member_RewardsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Do_Standard_Lynia Delivery Express_Member_Rewards">Les membres standards du programme de récompense des membres Lynia Delivery Express ont-ils droit à des avantages ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Do_Standard_Lynia Delivery Express_Member_Rewards">
       Oui, nos membres Premium standards bénéficient d’avantages exceptionnels tels qu’une réduction des frais de service, un accès aux livraisons hors taxe en passant par notre entrepôt de Tualatin, en Oregon, et 45 jours d’entreposage.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Do_I_get_phone_supportxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Do_I_get_phone_support">Ai-je droit à l’assistance téléphonique avec le programme d’abonnement Premium annuel ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Do_I_get_phone_support">
       L’assistance téléphonique est disponible pour tous les membres du programme de récompenses de niveau Gold, Platinum et Diamond. Tous les membres Lynia Delivery Express ont droit à une assistance gratuite par courrier électronique et chat.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "What_hours_is_phonexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#What_hours_is_phone">Quels sont les horaires de l’assistance téléphonique ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "What_hours_is_phone">
       L’assistance téléphonique est disponible de 7 h à 18 h, heure du Pacifique, du lundi au vendredi.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "When_can_I_choose_myxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#When_can_I_choose_my">Quand puis-je choisir mes avantages ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "When_can_I_choose_my">
       La période de sélection des avantages va du 1er au 31 janvier tous les ans. Vous pouvez toujours choisir vos avantages après le 31 janvier, mais ils ne prendront effet qu’au moment où vous le faites.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "How_do_I_selectxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#How_do_I_select">Comment choisir mes avantages ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "How_do_I_select">
       Connectez-vous à votre compte Lynia Delivery Express et cliquez sur « Mon compte» , « Mes récompenses » puis « Mes avantages ».
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "When_do_my_newxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#When_do_my_new">A quel moment mes nouveaux avantages prennent-ils effet ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "When_do_my_new">
       Ils sont effectifs dès que vous les sélectionnez. Si vous ne sélectionnez pas vos avantages pour la nouvelle année civile au cours de la période d’inscription de janvier, ceux de l’année précédente deviennent inactifs à compter du 1er février.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "What_if_I_miss_thexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#What_if_I_miss_the">Et si je rate la période d’inscription de janvier ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "What_if_I_miss_the">
       Vous pouvez choisir vos avantages à tout moment après le 31 janvier, mais ils entrent en vigueur seulement à partir de la date de sélection. Les avantages de l’année civile précédente ne sont plus actifs à compter du 1er février.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_change_my_benefits_oncexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_change_my_benefits_once">Puis-je modifier mes avantages une fois que je les ai choisis ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_change_my_benefits_once">
       Non, une fois sélectionnés, vous ne pouvez modifier vos avantages que pendant la période de sélection de l’année civile suivante, en janvier. Toutefois, si vous passez au niveau de récompenses suivant, vous pouvez sélectionner votre nouvel avantage à ce moment-là.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Can_I_lose_benefits_fromxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Can_I_lose_benefits_from">Puis-je perdre des avantages d’une année sur l’autre ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Can_I_lose_benefits_from">
       Vos avantages de l’année en cours découlent de votre activité de l’année précédente. Si vous accumulez moins de points de récompense et que vous vous qualifiez pour un niveau inférieur à celui des années précédentes, vous bénéficierez de moins d’avantages que par le passé.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "Do_my_benefits_expire_ifxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#Do_my_benefits_expire_if">Mes avantages expirent-ils si j’annule mon abonnement Premium annuel ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "Do_my_benefits_expire_if">
       Oui, votre abonnement annuel doit rester actif pour pouvoir utiliser vos avantages du programme de récompenses.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "I_have_benefits_fromxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#I_have_benefits_from">Il me reste des avantages du vieux programme de récompenses Lynia Delivery Express qui a été abandonné en 2016. Puis-je les garder ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "I_have_benefits_from">
       Vos anciens avantages disparaissent lorsque vous sélectionnez les nouveaux. Vos avantages issus de l’ancien programme disparaitront fin 2019 même si vous n’en sélectionnez pas de nouveaux.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "What_is_Glove_ServicexTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#What_is_Glove_Service">Qu’est-ce que le service White Glove ?
 <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "What_is_Glove_Service">
       Ce service exclusif pour nos membres Platinum et Diamond permet de parler à un agent Premium de Lynia Delivery Express, dans l’entrepôt, pour faire des demandes exceptionnelles. Ce service est disponible de 9 h à 17 h, heure du Pacifique, du lundi au vendredi.
<BR>


      </DIV>
     </DIV>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "benefitworkxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#benefitwork">Comment l‘avantage des 5 regroupements gratuits fonctionne-t-il? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "benefitwork">
       Vou obtiendrez 5 regroupements gratuits par mois (et jusqu‘à 5 colis par regroupement).<br>
Dans le cas de plus de 5 colis à regrouper en un seul, les 5 premiers seront gratuits et tous les autres seront à votre charge. Cela sera considéré comme l‘un de vos 5 regroupements gratuits.<br>
Si vous avez 2 colis à regrouper en un seul, ils seront tous les deux gratuits. Cela sera considéré comme l‘un de vos 5 regroupements gratuits.<BR>


      </DIV>
