     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "trackingnotupdatedxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#trackingnotupdated">Pourquoi mon suivi n'est-il pas encore mis à jour ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "trackingnotupdated">
       <strong>Possibility #1</strong>
<br>
Not all carriers provide tracking updates or they can be sporadic. We try to provide as much information as we are given by the carriers. Please review our <a href="#" target="_blank">Shipping Methods</a> page to see what carriers usually provide tracking updates.

<br>
<br>
<strong>Possibility #2</strong>
<br>
If you are using one of these carriers: USPS, RRD, Australian Post, Boxberry, AirMail Economy, Lynia Delivery Express Preferred Carrier and your package was in Tualatin, Oregon; we will transport it to Torrance for shipment out of LAX. This may take 3-5 business days. Tracking information will be updated once the package is in the hands of the carrier.
<br>
<br>
<strong>Possibility #3</strong>
<br>
Sometimes the local postal system takes over the tracking and does not send that information to USPS and Lynia Delivery Express.  In that case, please take the USPS tracking number you have been provided and enter that number into your local postal system website for tracking.  You should be able to see further updates to your package movement.
<br>
<br>

Please contact our customer service team via Email, <A HREF = "mailto:support@shipito.com">support@shipito.com</a> for additional questions.<BR>


