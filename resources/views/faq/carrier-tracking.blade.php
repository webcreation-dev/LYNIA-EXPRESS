    <H2>Mises à jour du suivi des colis</H2>

     <DIV CLASS = "panel panel-default help-topic">
      <DIV CLASS = "panel-title" ID = "howtotrackxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#howtotrack">How can I find my tracking number? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "howtotrack">
       All of your packages processed by Lynia Delivery Express can be found by clicking on the "Sent Packages" tab at the top of your account. Each package has a tracking number link which will open a new tab displaying all available tracking information for your package. All packages are assigned a tracking number but not all shipping methods provide tracking updates. To check to see if your carrier provides tracking, please visit our <a href="#" TARGET = "_blank">video</A> for more information.<BR>
       <IMG SRC = "shipito/fr/imgs/How-can-I-find-my-tracking-number-French.png" BORDER = "0" CLASS = "img-responsive">

      </DIV>
