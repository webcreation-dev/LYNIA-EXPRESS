      <DIV CLASS = "panel-title" ID = "msdsxTOP">
       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#msds">Qu'est-ce qu'une fiche MSDS (anglais), FDS (français) et comment puis-je en l’obtenir ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "msds">
       La fiche de données de sécurité (FDS) ou la fiche signalétique est un document qui contient des informations sur les produits potentiellement dangereux. De nombreux produits, en particulier dans l’électronique, contiennent des composés présentant des risques pour la santé ou l'environnement, ainsi que des risques d'incendie et de réactivité  lors de l'expédition. Une fiche signalétique / FDS est généralement requise pour les piles / batteries au lithium-ion ou les produits contenant des produits chimiques spécifiques.
<br>
<br>
Vous pouvez trouver la fiche MSDS / FDS en ligne avec le nom de l'article <strong> + </strong> le numéro du modèle <strong> + </strong> MSDS ou FDS.


<br>
<br>
Par exemple: "iPhone 7 MSDS"
<br>
<br>
Si vous ne trouvez pas la fiche en ligne, contactez le vendeur pour l’obtenir. Il vous faudra ensuite télécharger cette fiche sur votre compte Lynia Delivery Express en utilisant l'option «Fournir des informations».

<br>
<br>
<strong> <font size = "2"> REMARQUE IMPORTANTE : </font> </strong>
<br>
La fiche MSDS / FDS est requise, et ce quelle que soit la quantité de piles contenue dans votre colis, pour les pays suivants :
<br>
<br>
<font size = "2"> Australie, Bangladesh, Brunéi Darussalam, Chine, Hong Kong, Inde, Indonésie, Japon, Laos, Macao, Malaisie, Maldives, Nouvelle-Zélande, Pakistan, Philippines, Singapour, Corée du Sud, Sri Lanka, Taïwan, Thaïlande, Vietnam </font><BR>


