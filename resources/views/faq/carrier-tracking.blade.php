       <A data-toggle = "collapse" aria-expanded = "false" HREF = "#magazinesandletterscost">Comment calculer le coût d’envoi du courrier et des magazines ? <SPAN CLASS = "ga-caret"></SPAN></A>
      </DIV>
      <DIV CLASS = "panel-collapse collapse" ID = "magazinesandletterscost">
       Pour un calcul plus précis du coût d’envoi de votre courrier et de vos magazines, veuillez consulter notre calculateur d'expédition internationale. Nous utilisons des poids par défaut :
<br>
<br>
Courrier :
Poids par défaut : 0.1kg
Dimensions par défaut : 25cm X 12cm X 2.5cm
<br>
<br>
Magazines :
Poids par défaut : 0.7kg
Dimensions par défaut : 33cm X 22cm X 2.5cm
<br>
<br>
Le transfert du courrier et des magazines peut se faire soit de manière immédiate soit à la manière d’un colis standard. Choisissez l’une de ces deux options dans les paramètres de votre compte.
<br>
<br>
Si vous choisissez l’envoi immédiat de votre courrier et de vos magazines, nous vous les expédions dès récéption via Lynia Delivery Express Airmail (service postal bon marché comparable au First Class mail par USPS). Votre numéro de carte de crédit doit être déjà enregistré sur votre compte pour que nous puissions vous faire suivre le colis immédiatement. Le colis entrant doit être clairement indiqué comme magazine (il ne peut être caché dans une enveloppe).
<br>
<br>
Si vous choisissez de traîter votre courrier et vos magazines tel un nouveau colis, c’est ainsi que nous les entrerons sur votre compte lors de leur récéption. Il vous suffira alors de planifier l’envoi à partir de votre compte en choisissant l'option d'expédition de votre choix.<BR>


